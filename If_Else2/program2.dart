
void main(){
        int colorCode=2;

        switch(colorCode){
                case 1:
                        print("Violet");

                case 2:
                        print("Indigo");

                case 3:
                        print("Blue");
                case 4:
                        print("Green");

                case 5:
                        print("Yellow");

                case 6:
                        print("Orange");

                case 7:
                        print("Red");
                default:
                        print("Enter valid colorCode input");
        }
}



